package com.mygdx.game.screens.ui;

public class ButtonType
{
    final float distanceToCenter;
    final float glowAngularSpeed;
    final float glowScale;

    ButtonType(float distanceToCenter, float glowAngularSpeed, float glowScale)
    {
        this.distanceToCenter = distanceToCenter;
        this.glowAngularSpeed = glowAngularSpeed;
        this.glowScale = glowScale;
    }
}
